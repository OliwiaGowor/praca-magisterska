function [mul9, mul11, mul13, mul14] = get_mix_col_tables()
    mul9  = zeros(1, 256, 'uint8'); mul11 = zeros(1, 256, 'uint8');
    mul13 = zeros(1, 256, 'uint8'); mul14 = zeros(1, 256, 'uint8');
    for i = 0:255
        x = uint8(i);
        x2 = bitshift(x,1); if x>=128, x2=bitxor(x2,27); end
        x4 = bitshift(x2,1); if x2>=128, x4=bitxor(x4,27); end
        x8 = bitshift(x4,1); if x4>=128, x8=bitxor(x8,27); end
        mul9(i+1)=bitxor(x8,x); mul11(i+1)=bitxor(bitxor(x8,x2),x);
        mul13(i+1)=bitxor(bitxor(x8,x4),x); mul14(i+1)=bitxor(bitxor(x8,x4),x2);
    end
end
