function [w, Nr, s_box, inv_s_box, m9, m11, m13, m14] = aes_init(key)
% AES_INIT Precompute Key Schedule and S-Boxes for AES

    % Force key to be a flat vector
    key = uint8(key);
    key = key(:)'; 
    
    Nk = length(key) / 4;
    
    % Determine number of rounds
    if Nk == 4
        Nr = 10;
    elseif Nk == 6
        Nr = 12;
    elseif Nk == 8
        Nr = 14;
    else
        error('Nieprawidłowa długość klucza. Musi wynosić 16, 24 lub 32 bajty.');
    end
    
    Nb = 4;
    
    % Load S-Box tables
    s_box = get_sbox();
    inv_s_box = get_inv_sbox();
    
    % Key Expansion
    w = key_expansion(key, Nk, Nr, s_box, Nb);

    % Helper tables for MixColumns
    [m9, m11, m13, m14] = get_mix_col_tables();
end

function w = key_expansion(key, Nk, Nr, s_box, Nb)
    % Round constants (Rcon)
    r_con_vals = uint8([01 02 04 08 16 32 64 128 27 54]); 
    
    % Allocate expanded key matrix (4 rows)
    w = zeros(4, Nb * (Nr + 1), 'uint8');
    
    % 1. Copy initial key to the first columns
    for i = 1:Nk
        % Get 4 bytes and force column vector (4x1)
        block = key(((i-1)*4 + 1) : (i*4));
        w(:, i) = block(:); 
    end
    
    % 2. Generate the rest of the key
    for i = (Nk + 1) : (Nb * (Nr + 1))
        temp = w(:, i-1); % temp is 4x1
        
        if mod(i-1, Nk) == 0
            % RotWord
            temp = circshift(temp, -1);
            
            % SubWord
            % Use S-Box; ensure result is a column vector
            val = s_box(double(temp) + 1);
            temp = val(:); 
            
            % XOR with Rcon
            r_idx = (i-1)/Nk;
            if r_idx <= length(r_con_vals)
                r_c = [r_con_vals(r_idx); 0; 0; 0]; 
            else
                r_c = [0; 0; 0; 0]; 
            end
            temp = bitxor(temp, r_c);
            
        elseif (Nk > 6) && (mod(i-1, Nk) == 4)
            % SubWord (for AES-256)
            val = s_box(double(temp) + 1);
            temp = val(:); 
        end
        
        % Final XOR with column Nk positions back
        res = bitxor(w(:, i-Nk), temp);
        w(:, i) = res(:); 
    end
end

